#!/bin/bash
#
# Create transaction csv file for testing

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" >/dev/null 2>&1 && pwd )"

NUM_TX=10000

# TX FORMAT: 4200000000000000000000000000000000000000,4200000000000000000000000000000000000000,0,,,
FROM_ACCOUNT=0000000000000000000000000000000000000001
TO_ACCOUNT=4200000000000000000000000000000000000000
AMOUNT=0

TX_FILE=$SCRIPT_DIR/transactions.csv

rm -f $TX_FILE

for ((i=0; i<$NUM_TX; i++))
do
    echo $FROM_ACCOUNT,$TO_ACCOUNT,$AMOUNT,,, >> $TX_FILE
    FROM_ACCOUNT=$((FROM_ACCOUNT+1))
done
